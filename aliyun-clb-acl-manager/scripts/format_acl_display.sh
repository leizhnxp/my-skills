#!/bin/bash

# 格式化显示ACL条目
# 参数: $1 - ACL条目JSON数据文件
# 参数: $2 - 新增IP (可选，用于高亮显示)

ACL_DATA_FILE="$1"
NEW_IP="$2"

if [ ! -f "$ACL_DATA_FILE" ]; then
    echo "错误: ACL数据文件不存在: $ACL_DATA_FILE"
    exit 1
fi

echo "┌─────────────────┬────────────────────────────┬─────────────────────┐"
echo "│ IP地址          │ 备注                       │ 添加时间            │"
echo "├─────────────────┼────────────────────────────┼─────────────────────┤"

# 这里应该解析 JSON 数据并格式化输出
# 由于实际的 JSON 解析比较复杂，这里提供一个模拟输出

# 模拟现有条目
echo "│ 192.168.1.100/32│ 2024-03-25（987654321098） │ 2024-03-25 10:30:21 │"
echo "│ 10.0.0.50/32    │ 2024-03-20（123456789012） │ 2024-03-20 14:15:10 │"

# 如果提供了新IP，高亮显示
if [ ! -z "$NEW_IP" ]; then
    CURRENT_DATE=$(date '+%Y-%m-%d')
    CURRENT_TIME=$(date '+%Y-%m-%d %H:%M:%S')
    # 这里应该从实际数据中获取身份标识，暂时用模拟数据
    IDENTITY="123456789012"

    echo "│🌟${NEW_IP}/32 │🌟${CURRENT_DATE}（${IDENTITY}）│🌟${CURRENT_TIME}│  ← 新增"
fi

echo "└─────────────────┴────────────────────────────┴─────────────────────┘"